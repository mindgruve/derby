<?php

namespace Derby\Media\KeyProvider;

use Derby\Interfaces\FileInterface;
use Derby\Interfaces\KeyProviderInterface;

class RandomKey implements KeyProviderInterface
{
    public function generateKey(FileInterface $file = null)
    {
        return sprintf(
            '%04X%04X-%04X-%04X-%04X-%04X%04X%04X',
            mt_rand(0, 65535),
            mt_rand(0, 65535),
            mt_rand(0, 65535),
            mt_rand(16384, 20479),
            mt_rand(32768, 49151),
            mt_rand(0, 65535),
            mt_rand(0, 65535),
            mt_rand(0, 65535)
        );
    }
}
